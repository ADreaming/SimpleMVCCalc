package MVCCalc;

public class Model {

	protected static int add(int i, int j){
		return i + j;
	}
	
	protected static int subtract(int i, int j){
		return i - j;
	}
	
	protected static int multiply(int i, int j){
		return i * j;
	}
	
	protected static int divide(int i, int j){
		return i / j;
	}
	
}
